#include <iostream>
using namespace std;

int main()
{
    // Relational Operators.

    int a = 5, b = 6;
    cout << (a > b) << endl;  // Greater than
    cout << (a < b) << endl;  // Less than
    cout << (a >= b) << endl; // Greater than or equal to
    cout << (a <= b) << endl; // Less than or equal to
    cout << (a == b) << endl; // Equal to
    cout << (a != b) << endl; // Not equal to
    return 0;
}